    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validateWithBag('storeComment',[
            'body' => ['string','required'],
            'post_id' => ['required','exists:posts,id'],
            'parent_id' => ['nullable','bail','exists:comments,id']
        ]);
        $request->user()->comments()->create([
            'body' => $request->body,
            'post_id' => $request->post_id,
            'parent_id' => !is_null($request->parent_id) ? $request->parent_id : 0
        ]);
        return redirect()->route('post.read', ['post' => Post::firstWhere('id',$request->post_id)->slug])->with(['message' => 'Comment posted']);
    }

